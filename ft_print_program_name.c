#include <unistd.h>

int	main(int c, char **v)
{
	int	i;

	i = 0;
	c = 0;
	while (v[c][i] != '\0')
	{
		write(1, &v[c][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
