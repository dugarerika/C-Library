#include <unistd.h>

void	ft_putnbr(int a)
{
	int	c;
	int	d;
	int	t;

	if (a >= 0 && a <= 9)
	{
		c = a + '0';
		write(1, &c, 1);
	}
	else
	{
		d = a % 10;
		c = d + '0';
		t = a / 10;
		ft_putnbr(t);
		write(1, &c, 1);
	}
}

